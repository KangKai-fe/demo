// inject bower components
gulp.task('wiredep', () => {
  gulp.src('app/styles/*.scss') // 首先拿到sass文件
    .pipe(wiredep({             // 处理其对bower_components依赖
      ignorePath: /^(\.\.\/)+/
    }))
    .pipe(gulp.dest('app/styles')); // 输出

  gulp.src('app/*.html')
    .pipe(wiredep({
      exclude: ['bootstrap-sass'], // 排除
      ignorePath: /^(\.\.\/)*\.\./
    }))
    .pipe(gulp.dest('app'));
});

